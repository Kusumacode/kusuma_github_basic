package Basic_program;

public class operators 
{
	public static void main(String[] args) 
	{
		int n1=5;
		int n2=6;
		
		int sum=n1+n2;
		int sub=n1-n2;
		int mul=n1*n2;
		int div=n2/n1;
		int mod=n2%n1;
		
		System.out.println(sum);
		System.out.println(sub);
		System.out.println(mul);
		System.out.println(div);
		System.out.println(mod);
	}
}
